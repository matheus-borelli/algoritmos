using System;

class DeclaracaoVariaveis
{
	static void Main()
	{
		/*
		Declaracao Variável
		*/
		//Inicialização da Variável
		int a;
		
		a = 22;
		Console.WriteLine(a);
		
		//Reatribuição de valor na Variável
		a = 56;
		Console.WriteLine(a);
		//Outras formas de declaração de Variáveis
		//Declaração e Inicialização a variavel
		int b= 925;
		Console.WriteLine(b);
		
		int y = 10, x = 20;
		int z = y + x;
		Console.WriteLine(z);
	}		
}	